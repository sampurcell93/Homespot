			<h3 id="started">Getting Started</h3>
			<p>Spotted lets you share epiphonic moments in your life. Spotted is a tool to get you the information you most care about. We do this by delivering content that is based on what you and your friends spot[1]. The more you spot, the more you benefit.</p>

