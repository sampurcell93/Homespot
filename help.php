			<h3 id="concepts">Concepts &amp; Terminology</h3>
			<p>Spotted uses certain words to describe certain actions throughout the app.</p>
			<dl class='terms'>
				<dt>Spot</dt>
					<dd>A spot is a unique event placed on the map by a user. If Mary Swan spotted a food truck, then her spot is the food truck.</dd>
				<dt>Respot</dt>
					<dd>When someone respots a spot, they're saying that (1) they have physically seen the original spot, or (2) they are in the general vicinity of the spot.</dd>
				<dt>Time Duration</dt>
					<dd>Each spot stays on the map for 30 minutes at a time. Once a spot is respotted, its duration is extended by 10 minutes. This brief time span allows the given event to feel authenic.</dd>
			</dl>

			<h3 id="nonarchival">Non-Archival</h3>
			<p>We built Spotted because we wanted to share an opportunity to be yourself. Sharing information on the internet had always had a certain stigma attached to it. On Spotted, when you spot something, it will only be visible for a short period of time, and once the duration of the pin has expired, it disappears altogether. Why? Because we understand that sometimes you just want to share without feeling the pressure of accumulating a permanent archive of your posts. This is why we made Spotted.</p>
			<p>What if I want to see my past spots? Currently, we do not offer this feature as it is counterintuitive with our idea. In the future, if it means saving the app, we may consider it.</p>

